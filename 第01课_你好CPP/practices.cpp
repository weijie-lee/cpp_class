// ---- Practice 3: Arithmetic Expressions (Advanced) ----
// Let the computer calculate three expressions and print each on a separate line
#include <iostream>
using namespace std;

int main() {
    cout << "100 + 200 = " << 100 + 200 << endl;
    cout << "50 - 18 = " << 50 - 18 << endl;
    cout << "7 * 8 = " << 7 * 8 << endl;
    return 0;
}

/*
 * Expected Output:
 * 100 + 200 = 300
 * 50 - 18 = 32
 * 7 * 8 = 56
 */

